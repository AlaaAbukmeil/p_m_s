function extractValuesFx(text: any) {
  let lines = text.split("\n");
  let output: any = {};

  lines.forEach((line: any) => {
    let colonIndex = line.indexOf(":");
    let key, value;
    if (colonIndex !== -1) {
      key = line.slice(0, colonIndex).trim();
      value = line.slice(colonIndex + 1).trim();
      output[key] = value;
    }
  });
  output = formatFxTrades(output);
  return output;
}
